# 1. Assign specific values to two variables, number1 and number2.
# 2. Perform addition, subtraction, and multiplication on these numbers.
# 3. Print the results of these operations in a human-readable format.

number1 = 10
number2 = 5

# Addition
add = number1 + number2
result = add
print(f"The addition of {number1} and {number2} is {result}")

# Subtraction
subtract = number1 - number2
result = subtract
print(f"The subtraction of {number1} and {number2} is {result}")

# Multiplication
multiply = number1 * number2
result = multiply
print(f"The multiplication of {number1} and {number2} is {result}")

